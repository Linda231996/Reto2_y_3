    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        //3. Capturar los valores que ingresa el usuario
        String username = txtUsername.getText(); //getText para que reciba lo que se digita
        String password = txtPassword.getText(); //como en el ej de YouTube

        //4. Mostramos lo que arroja para comprobar que funciona
        System.out.println("Usuario: " + username + ", Contraseña: " + password);
        //5. Validar que los campos no esten vacíos
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Faltan campos por diligenciar", "Información", JOptionPane.WARNING_MESSAGE);
        } else {
            if (username.equals("MisionTIC") && password.equals("Ciclo2")) {
                JOptionPane.showMessageDialog(null, "Bienvenido al sistema Misión TIC", "Bienvenido", WIDTH);
                //6. Se hace visible el nuevo JFrame de navegación 
                UserMenu usermenu = new UserMenu(); //Nombre del JFrame //¿Por qué se instancia aquí?
                usermenu.setVisible(true);
                //7. Eliminar el frame de login para que no se muestre después de lo anterior
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usuario y/o contraseña incorrecta", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

